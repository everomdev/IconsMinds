public extension IconsMinds {

    /// Convierte "simple-icon-layers" (API) a `.simple_icon_layers`
    init?(name: String) {
        // "simple-icon-layers" -> "simple_icon_layers"
        let normalized = name
            .replacingOccurrences(of: "-", with: "_")

        self.init(caseInsensitiveRawCaseName: normalized)
    }

    /// Devuelve el car√°cter Unicode real del icono
    var unicodeScalar: String {
        let hex = rawValue
            .replacingOccurrences(of: "uni", with: "")
            .trimmingCharacters(in: .whitespacesAndNewlines)

        guard let value = UInt32(hex, radix: 16),
              let scalar = UnicodeScalar(value) else {
            return ""
        }
        return String(scalar)
    }

    /// Helper: inicializa por nombre del case (no por rawValue)
    private init?(caseInsensitiveRawCaseName name: String) {
        // Compara con el nombre del case (String(describing:))
        if let match = Self.allCases.first(where: { String(describing: $0).lowercased() == name.lowercased() }) {
            self = match
            return
        }
        return nil
    }
}

